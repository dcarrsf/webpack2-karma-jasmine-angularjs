/**
 * *******************************************************
 *
 *  The Calculator class holds static methods for testing calculations.
 *
 *  @author     Dan Carr
 *  @date       February 2017
 *
 * *******************************************************
 */

export default class Calculator {
  static add(arg1, arg2) {
    return arg1 + arg2;
  }
  static subtract(arg1, arg2) {
    return arg1 - arg2;
  }
}
